#include "func.h"
/**
 * add - add numbers
 * @a: integer
 * @b: integer
 * Return: integer
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two numbers
 * @a: integer
 * @b: integer
 * Return: integer
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiply
 * @a: integer
 * @b: integer
 * Return: integer
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divide
 * @a: integer
 * @b: integer
 * Return: integer
 */
int div(int a, int b)
{
	return (a / b);
}
